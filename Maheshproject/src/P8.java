//wap to find the product of all the number in the range 1-5
//wap too find factorial of 5.
public class P8 {
    public static void main(String[] args) {
        int mult= 1;
        for(int i=1; i<=5;i++)
        {
            mult=mult * i;
        }
        System.out.println(mult);
    }
}
